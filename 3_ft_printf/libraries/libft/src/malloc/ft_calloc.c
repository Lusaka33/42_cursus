#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*var;

	if (count == 0 || size == 0)
	{
		count = 1;
		size = 1;
	}
	if (!(var = malloc(sizeof(char) * (count * size))))
		return (NULL);
	ft_bzero((void*)var, count * size);
	return ((void*)var);
}
