#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list *liste;

	liste = NULL;
	if (!(liste = malloc(sizeof(t_list))))
		return (NULL);
	liste->content = content;
	liste->next = NULL;
	return (liste);
}
